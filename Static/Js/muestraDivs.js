/* Función para mostrar u ocultar el formulario para personas*/

function muestraFormPersona(valor) 
{
	if(valor.value=="0") 
	{
		document.getElementById("nvaPersona").style.display = "block"; 
	} 
	else 
	{
		document.getElementById("nvaPersona").style.display = "none";
	} 
	 
}